
public class TipoVar {
	public static void main(String[] args) {
		System.out.println("**** UYso de var en Java");
		
		// Sin el uso de var
		
		String nombre1 = "Juan";
		System.out.println("Nombre1= "+ nombre1);
		
		//con Var
		
		var nombre2 = "Carlos";
		System.out.println("nombre2 = " + nombre2);
	}
	
}
